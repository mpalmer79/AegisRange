_RED_VERBS: tuple[VerbSpec, ...] = ()


# ---------------------------------------------------------------------------
# Public lookup
# ---------------------------------------------------------------------------


def verbs_for(perspective: Perspective) -> tuple[VerbSpec, ...]:
    """Return all verbs visible to ``perspective`` (universal + role)."""
    role_specific = _BLUE_VERBS if perspective == "blue" else _RED_VERBS
    return _UNIVERSAL_VERBS + role_specific


def find_verb(
    name: str, subcommand: str | None, perspective: Perspective
) -> VerbSpec | None:
    for v in verbs_for(perspective):
        if v.name == name and v.subcommand == subcommand:
            return v
    return None
