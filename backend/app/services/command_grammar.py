"""Mission command grammar.

Declarative specs for the verbs a player can type in the mission
console. The grammar is versioned data — parsers, help content, and
dispatchers all read from the same source of truth so adding a verb is
a single-file change.

Phase 3a ships the Blue-team verb set needed to complete scn-auth-001:
``alerts list`` / ``alerts show`` / ``correlate`` / ``contain session``,
plus the universal ``help`` / ``hint`` / ``status``. Red-team verbs
land in Phase 3b.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Literal

Perspective = Literal["red", "blue", "both"]
FlagType = Literal["str", "int", "csv", "choice"]


@dataclass(frozen=True)
class FlagSpec:
    name: str
    type: FlagType = "str"
    required: bool = False
    default: object | None = None
    choices: tuple[str, ...] | None = None
    description: str = ""


@dataclass(frozen=True)
class PositionalSpec:
    name: str
    description: str = ""
    required: bool = True


@dataclass(frozen=True)
class VerbSpec:
    """A single verb in the grammar.

    ``name`` is always the first token. ``subcommand`` — when present —
    is the second token; together (name, subcommand) identifies the
    verb uniquely per perspective.
    """

    name: str
    subcommand: str | None = None
    handler: str = ""
    perspective: Perspective = "both"
    description: str = ""
    usage: str = ""
    positional: tuple[PositionalSpec, ...] = field(default_factory=tuple)
    flags: tuple[FlagSpec, ...] = field(default_factory=tuple)
    # Short one-line summary used in `help` output; defaults to the
    # first sentence of ``description`` if unset.
    summary: str = ""

    @property
    def key(self) -> str:
        return self.name if self.subcommand is None else f"{self.name} {self.subcommand}"

    def effective_summary(self) -> str:
        if self.summary:
            return self.summary
        if self.description:
            first_line, _, _ = self.description.partition("\n")
            return first_line
        return ""


